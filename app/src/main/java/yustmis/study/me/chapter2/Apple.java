package yustmis.study.me.chapter2;

public class Apple {

    private Color color;
    private Integer weight;

    

    public Apple() {
    }

    public Apple(Integer weight) {
        this.weight = weight;
    }

    public void setColor(Color color) {
        this.color = color;
    }

    public Color getColor() {
        return color;
    }

    public Integer getWeight() {
        return weight;
    }

    public void setWeight(Integer weight) {
        this.weight = weight;
    }

    
    
}
